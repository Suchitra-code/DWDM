a = np.arange(10) ** 3
print("Let's consider the elements:", a)
print("The output of a[2] is:", a[2])
print("The output of a[2:4] is:", a[2:4])
print("The output of a[2:3] is:", a[2:3])
print("The output of a[-1] is:", a[-1])
print("The output of a[-2] is:", a[-2])
print("The output of a[-5:-2] is:", a[-5:-2])
